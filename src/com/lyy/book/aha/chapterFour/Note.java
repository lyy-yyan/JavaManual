package com.lyy.book.aha.chapterFour;

public class Note {
    private int x;
    private int y;

    Note() {}
    Note(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}